CREATE TABLE TIPO_ARTICULO (
    CODTIPOPRODUCTO INT NOT NULL PRIMARY KEY,
    DESCRIPCION VARCHAR(50)
);

CREATE TABLE GRUPO (
    CODGRUPO INT NOT NULL PRIMARY KEY,
    DESCRIPCION VARCHAR(50)
);

CREATE TABLE MARCA (
    CODMARCA INT NOT NULL PRIMARY KEY,
    DESCRIPCION VARCHAR(100)
);

CREATE TABLE ARTICULO (
    CODARTICULO INT NOT NULL PRIMARY KEY,
    DESCRIPCION VARCHAR(100),
    STOCK NCHAR(10),
    CODPROVEEDOR INT,
    CODMARCA INT,
    CODGRUPO INT,
    PRECIO DECIMAL(18, 2),
    CODTIPO INT,
    IMAGEN IMAGE,
    CODPAIS INT,
    FOREIGN KEY (CODPROVEEDOR) REFERENCES PROVEEDOR(CODPROVEEDOR),
    FOREIGN KEY (CODMARCA) REFERENCES MARCA(CODMARCA),
    FOREIGN KEY (CODGRUPO) REFERENCES GRUPO(CODGRUPO),
    FOREIGN KEY (CODTIPO) REFERENCES TIPO_ARTICULO(CODTIPOPRODUCTO),
    FOREIGN KEY (CODPAIS) REFERENCES PAIS(CODPAIS)
);